function setNumberMask(input, value) {

	input.value = '';
	input.dataset.valid = 'false';
	input.closest('form').classList.remove('form-active');

	$(input).unmask();
	$(input).mask(value, {
		autoclear: false,
	});

	input.placeholder = value;

}


function setFlagImg(img, countrycode) {
	img.src = './public/img/flags/' + countrycode.toLowerCase() + '.svg';
	img.alt = countrycode.toLowerCase();
}

